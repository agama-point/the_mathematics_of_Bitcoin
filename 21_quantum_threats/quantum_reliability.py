# Total reliability of N entangled qubits
# Errors are independent and probabilities multiply

import math

Ns = [1, 5, 10, 50, 100, 1000, 5000]
ps = [0.9, 0.99, 0.999, 0.9999, 0.99999]

# Header
header = "N".ljust(8) + "".join(f"{p:<12}" for p in ps)
print(header)

# Rows
for N in Ns:
    row = f"{N:<8}"
    for p in ps:
        total = p ** N
        row += f"{total:<12.6f}"
    print(row)

